<script setup lang="ts" name="Online">
import { forceLogout, list as initData } from "@/api/monitor/online"
import type { SysUserOnline, OnlineQueryParams } from '@/types/api/monitor/online'

const { proxy } = getCurrentInstance()

const onlineList = ref<SysUserOnline[]>([])
const loading = ref<boolean>(true)
const showSearch = ref<boolean>(true)
const ids = ref<number[]>([])
const single = ref<boolean>(true)
const multiple = ref<boolean>(true)
const total = ref<number>(0)

const data = reactive({
  queryParams: {
    pageNum: 1,
    pageSize: 10,
    ipaddr: undefined,
    userName: undefined
  } as OnlineQueryParams
})

const { queryParams } = toRefs(data)

/** 查询登录日志列表 */
function getList() {
  loading.value = true
  initData(queryParams.value).then(response => {
    onlineList.value = response.rows
    total.value = response.total
    loading.value = false
  })
}

/** 搜索按钮操作 */
function handleQuery() {
  queryParams.value.pageNum = 1
  getList()
}

/** 重置按钮操作 */
function resetQuery() {
  proxy.resetForm("queryRef")
  handleQuery()
}

/** 多选框选中数据 */
function handleSelectionChange(selection: SysUserOnline[]) {
  ids.value = selection.map(item => item.tokenId!)
  single.value = selection.length != 1
  multiple.value = !selection.length
}

/** 强退按钮操作 */
function handleForceLogout(row: SysUserOnline) {
  proxy.$modal.confirm('是否确认强退名称为"' + row.userName + '"的用户?').then(function () {
    return forceLogout(row.tokenId!)
  }).then(() => {
    getList()
    proxy.$modal.msgSuccess("删除成功")
  }).catch(() => {})
}

getList()
</script>

